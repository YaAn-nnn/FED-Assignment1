function openNav() {
    // Make the sidebar visible
    document.getElementById("sidebar").style.display = "block";
    document.getElementById("sidebar").style.width = "250px"; // Slide-in effect
}

function closeNav() {
    // Hide the sidebar
    document.getElementById("sidebar").style.display = "none";
    document.getElementById("sidebar").style.width = "0"; // Slide-out effect
}
